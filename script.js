/* ================================
   IMAGE PREVIEW
================================ */

document.getElementById("imageUpload").onchange = function (e) {
  const file = e.target.files[0];

  if (!file) return;

  const reader = new FileReader();

  reader.onload = function () {
    const preview = document.getElementById("preview");

    preview.src = reader.result;

    preview.style.display = "block";
  };

  reader.readAsDataURL(file);
};

/* ================================
   CONVERT IMAGE TO BASE64
================================ */

function convertImage(file) {
  return new Promise((resolve, reject) => {
    const reader = new FileReader();

    reader.readAsDataURL(file);

    reader.onload = () => {
      const base64 = reader.result.split(",")[1];

      resolve(base64);
    };

    reader.onerror = (error) => reject(error);
  });
}

/* ================================
   COPY TEXT FUNCTION
================================ */

function copyText(text) {
  navigator.clipboard.writeText(text);

  alert("Copied to clipboard");
}

/* ================================
   RENDER OUTPUT
================================ */

function renderOutput(text) {
  const output = document.getElementById("output");

  output.innerHTML = "";

  /* EXTRACT CAPTIONS */

  const captionRegex = /CAPTION_\d:\s*([\s\S]*?)(?=CAPTION_\d:|HASHTAGS:)/g;

  let captions = [];

  let match;

  while ((match = captionRegex.exec(text)) !== null) {
    captions.push(match[1].trim());
  }

  /* EXTRACT HASHTAGS */

  const hashtagMatch = text.match(/HASHTAGS:\s*(.*)/);

  let hashtags = [];

  if (hashtagMatch) {
    hashtags = hashtagMatch[1].split(" ");
  }

  /* EXTRACT KEYWORDS */

  const keywordMatch = text.match(/KEYWORDS:\s*(.*)/);

  let keywords = [];

  if (keywordMatch) {
    keywords = keywordMatch[1].split(",").map((k) => k.trim());
  }

  /* CAPTION CARDS */

  captions.forEach((caption) => {
    output.innerHTML += `
<div class="caption-card">

<p>${caption}</p>

<button class="copy-btn" onclick="copyText(\`${caption}\`)">
Copy Caption
</button>

</div>
`;
  });

  /* HASHTAG SECTION */

  output.innerHTML += `
<div class="hashtag-section">

<h5 class="hashtag-title">Hashtags</h5>

<div class="hashtag-list">

${hashtags.map((tag) => `<span class="hashtag">${tag}</span>`).join("")}

</div>

</div>
`;

/* KEYWORD SECTION */

output.innerHTML += `
<div class="hashtag-section">

<h5 class="hashtag-title">Keywords</h5>

<div class="hashtag-list">

${keywords.map((key) => `<span class="hashtag">${key}</span>`).join("")}

</div>

</div>
`;
}

